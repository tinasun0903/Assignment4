public class Calculator {

    public int add(int num1, int num2){
        return num1 + num2;

    }
    public int sub(int num1, int num2){
        return num1 - num2;
    }
    public int multiple(int num1, int num2){
        return num1*num2;
    }
    public int division(int num1, int num2){
        return num1/num2;
    }
}

